package in_class;

public class Show_string_index_of 
{

	public static void main(String[] args) 
	{
		String mySearch;
		mySearch = "area codes in the US? 7";
		System.out.println("My search string is " + mySearch);
		System.out.println("The index for the character t is at " + mySearch.indexOf('t'));
		System.out.println("The index for the character a is at " + mySearch.indexOf('a'));

	}

}
