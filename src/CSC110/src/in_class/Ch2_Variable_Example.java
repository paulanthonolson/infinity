package in_class;

public class Ch2_Variable_Example {

	public static void main(String[] args) {
	  	System.out.println ("begin program");
		
	  	int baseChar; //variable called baseChar and the data type is an integer.
		int headChar; //variable called headChar and the data type is an integer.
		
		baseChar = 0; //variable is baseChar, my data is 0 and the instruction is baseChar = 0
		headChar = 1; //variable is headChar, my data is 1 and the instruction is headChar = 1
		
		System.out.println (baseChar);
		System.out.println (headChar);
		
		System.out.println ("end program");

	}

}
